    public ASAgui() {
        initComponents();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        int[] arraySizes = {100, 1000, 10000, 100000, 1000000};
        String[] algorithms = {"Binary Search", "Interpolation Search"};

        DecimalFormat df = new DecimalFormat("#.########");

        for (int i = 0; i < arraySizes.length; i++) {
            model.setValueAt(i + 1, i, 0); // Row number
            model.setValueAt(arraySizes[i], i, 1); // Array size

            int[] array = new int[arraySizes[i]];
            for (int j = 0; j < arraySizes[i]; j++) {
                array[j] = (int) (Math.random() * 10000000);
            }

            for (int k = 0; k < algorithms.length; k++) {
                int[] arrayCopy = Arrays.copyOf(array, array.length);
                Arrays.sort(arrayCopy); // Sorting for binary search

                double start = System.nanoTime();
                if (k == 0) {
                    // Binary Search
                    int find = 7; // Example value to find
                    larik.BinarySearch(arrayCopy, find);
                } else {
                    // Interpolation Search
                    int find = 7; // Example value to find
                    larik.interpolationSearch(arrayCopy, find);
                }
                double finish = System.nanoTime();
                double elapsedTime = (finish - start) / 1_000_000_000; // Convert to seconds

                // Format the elapsed time in seconds
                String formattedTime = df.format(elapsedTime);
                model.setValueAt(formattedTime, i, k + 2); // Column 2 for Binary Search, Column 3 for Interpolation Search
            }
        }
    

    // Other methods and GUI initialization code
}




